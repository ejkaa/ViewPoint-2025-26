-- Optional but recommended
create extension if not exists "pgcrypto";

-- =========================================================
-- 1) PROFILES
-- =========================================================
create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  username text unique not null,
  full_name text,
  avatar_url text,
  bio text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- =========================================================
-- 2) ENTRIES
-- One row = one user tracking one TMDB title
-- =========================================================
create table if not exists public.entries (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,

  tmdb_id integer not null,
  media_type text not null check (media_type in ('movie', 'tv')),

  status text not null default 'plan_to_watch'
    check (status in ('plan_to_watch', 'watching', 'completed', 'dropped', 'on_hold')),

  episodes_watched integer not null default 0 check (episodes_watched >= 0),
  rating integer check (rating between 1 and 10),
  note text,

  started_at timestamptz,
  finished_at timestamptz,

  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  unique (user_id, tmdb_id, media_type)
);

-- =========================================================
-- 3) LISTS
-- Custom user lists
-- =========================================================
create table if not exists public.lists (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,

  title text not null,
  description text,

  is_public boolean not null default false,

  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  unique (user_id, title)
);

-- =========================================================
-- 4) LIST_ENTRIES
-- Join table between lists and TMDB items
-- =========================================================
create table if not exists public.list_entries (
  id bigint generated by default as identity primary key,
  list_id bigint not null references public.lists(id) on delete cascade,

  tmdb_id integer not null,
  media_type text not null check (media_type in ('movie', 'tv')),

  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  unique (list_id, tmdb_id, media_type)
);

-- =========================================================
-- INDEXES
-- =========================================================
create index if not exists entries_user_id_idx on public.entries(user_id);
create index if not exists entries_tmdb_idx on public.entries(tmdb_id, media_type);
create index if not exists lists_user_id_idx on public.lists(user_id);
create index if not exists list_entries_list_id_idx on public.list_entries(list_id);

