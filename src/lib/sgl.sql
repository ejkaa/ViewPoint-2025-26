create policy "list_entries_select_if_owner_or_public_list"
on public.list_entries
for select
to authenticated
using (
  exists (
    select 1
    from public.lists l
    where l.id = list_entries.list_id
      and (
        l.user_id = (select auth.uid())
        or l.is_public = true
      )
  )
);

